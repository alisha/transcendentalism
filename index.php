<br><br>
<p>The TED Talk above inspired me to slow down my life, and to stop rushing everything I did. In addition, a nature walk and journaling activity showed me how fast my thoughts fly by - I realized that I needed to change something in order to slow myself down. So, I decided to meditate every day using the Headspace app, which would let me slow down and spend more time alone.</p>
<p>In addition, I was amazed at how much beauty and peace Thoreau found in nature. I don't spend that much time outside, especially with New England weather, but I decided to try and discover that beauty around me through photography.</p>

