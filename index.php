<h1>A Journey into Transcendentalism -Introduction</h1>

<h2 id="purpose">Purpose</h2>
<p>The goal of this project was to apply Transcendentalist principles into our own life to gain a better understanding of the philosopy. After reading texts such as "Self Reliance" by Ralph Waldo Emerson and much of <i>Walden</i> by Henry David Thoreau, we created personal, modern versions of Transcendentalism.</p>

<h2 id="principles">Principles embodied</h2>
<p>For four days, I had two goals: (1) to spend time photographing nature, and (2) to meditate every day. I took photos around my house, and I took care to pick up on smaller details that would otherwise be overlooked. In this way, I would appreciate both the "leafless root" and the "full-blown flower" (Emerson).</p>

<h2 id="before">Before</h2>
<p>Before this project, I felt that I already practiced aspects of Transcendentalism, such as non-conformity and introspection. However, I didn't spend that much time outside, and I didn't slow my life down enough.</p>

<h2 id="inspiration">Inspiration</h2>
